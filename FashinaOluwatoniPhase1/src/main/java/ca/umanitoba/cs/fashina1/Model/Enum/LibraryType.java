package ca.umanitoba.cs.fashina1.Model.Enum;

/**
 * Represents the different types of libraries in the library system.
 * Each library type serves a specific community or organizational purpose,
 * from academic institutions to public access and recreational facilities.
 * This enum provides a standardized classification system for library institutions.
 */
public enum LibraryType {
    /** Academic library serving university students*/
    University,

    /** Public library open to all*/
    Public,

    /** Informal library setting focused on leisure */
    Casual,

    /** Library for recareation and games */
    Recreation
}