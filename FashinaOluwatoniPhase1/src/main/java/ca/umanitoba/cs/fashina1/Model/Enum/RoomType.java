package ca.umanitoba.cs.fashina1.Model.Enum;

/**
 * Represents the different types of rooms available in the library system.
 * Each room type serves a specific  purpose

 */
public enum RoomType {
    /** Area designated for storing and organizing library media collections on shelves */
    ShelvingArea,

    /** Quiet space intended for individual reading, study, and academic work */
    ReadingArea,

    /** Room equipped with computers and technology resources for digital access */
    ComputerRoom,

    /** Space designed for leisure activities, games, and social interactions */
    RecreationArea
}